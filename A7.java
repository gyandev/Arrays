package com.gyan.arrays;

public class A7 {
	public static void main(String[] args) {
		char[] ch=new char[4];
		System.out.println(ch.length);
		for(char x1 : ch)
		{
			System.out.println(x1);
		}
		for(int i=0;i<ch.length;i++)
		{
			System.out.println(ch[i]);
		}

		
	}

}


