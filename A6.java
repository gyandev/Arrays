package com.gyan.arrays;

public class A6 {
	public static void main(String[] args) {
		String[] x=new String[4];
		System.out.println(x.length);
		for(String x1 : x)
		{
			System.out.println(x1);
		}
		for(int i=0;i<x.length;i++)
		{
			System.out.println(x[i]);
		}

		
	}

}


